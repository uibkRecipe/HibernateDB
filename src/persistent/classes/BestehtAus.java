package persistent.classes;

public class BestehtAus {
	int zut_typ_id;
	int rez_id;
	String menge;
	
	public BestehtAus(){
		
	}

	public int getZut_typ_id() {
		return zut_typ_id;
	}

	public void setZut_typ_id(int zut_typ_id) {
		this.zut_typ_id = zut_typ_id;
	}

	public int getRez_id() {
		return rez_id;
	}

	public void setRez_id(int rez_id) {
		this.rez_id = rez_id;
	}

	public String getMenge() {
		return menge;
	}

	public void setMenge(String menge) {
		this.menge = menge;
	}
}
